#include <iostream>

int main() {
    bool prawda = true;
    bool falsz = false;
    std::cout << prawda << '\n';
    std::cout << falsz << '\n';

    // < - mniejsze
    // <= - mniejsze lub równe
    // > - większe
    // >= - większe lub równe
    // == - równe
    // != - różne
    bool b = 3 == 3;
    std::cout << b << '\n';

    return 0;
}